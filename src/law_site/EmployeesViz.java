package law_site;

public class EmployeesViz {
	private Short id;
	private Short person_id;
	private Short position_id;
	private String adress;
	private String person_name;
	private String person_surname;
	private String person_patronymic;
	
	public Short getId() {
		return id;
	}
	public void setId(Short id) {
		this.id = id;
	}
	public Short getPerson_id() {
		return person_id;
	}
	public void setPerson_id(Short person_id) {
		this.person_id = person_id;
	}
	public Short getPosition_id() {
		return position_id;
	}
	public void setPosition_id(Short position_id) {
		this.position_id = position_id;
	}
	public String getAdress() {
		return adress;
	}
	public void setAdress(String adress) {
		this.adress = adress;
	}
	public String getPerson_name() {
		return person_name;
	}
	public void setPerson_name(String person_name) {
		this.person_name = person_name;
	}
	public String getPerson_surname() {
		return person_surname;
	}
	public void setPerson_surname(String person_surname) {
		this.person_surname = person_surname;
	}
	public String getPerson_patronymic() {
		return person_patronymic;
	}
	public void setPerson_patronymic(String person_patronymic) {
		this.person_patronymic = person_patronymic;
	}

}
